    public ArtNetSender(String sourceIP, Integer sourcePort, String destinationIP, Integer destinationPort) {
        this.sourceIP = sourceIP;
        this.sourcePort = sourcePort;
        this.destinationIP = destinationIP;
        this.destinationPort = destinationPort;
    }

    public void start() throws SocketException, IOException {

        if (sourcePort != null) {
            theUDPSocket = new DatagramSocket(sourcePort);
        } else {
            theUDPSocket = new DatagramSocket();
        }
        theUDPSocket.setReuseAddress(true); //in case port has not closed after last use.
        InetAddress InetDestinationIP = InetAddress.getByName(destinationIP);

        runThread = true;
        sendThread = new Thread() {
            @Override
            public void run() {
                while (runThread) {
                    try {
                        Thread.sleep(25); // about 40Hz update
                    } catch (InterruptedException ex) {
                    }

                    try {
                        theUDPSocket.send(new DatagramPacket(dataPacket, dataPacket.length, InetDestinationIP, destinationPort)); //send the packet
                    } catch (IOException ex) {
                        System.out.println(ex.toString());
                    }
                }
                theUDPSocket.close();
            }
        };
        sendThread.start();

    }

    public void stop() {
        runThread = false;
    }

    public void setDMXValues(int firstChannel, byte[] values) {
        firstChannel--;
        for (byte value : values) {
            if (firstChannel > 511) {
                break;
            }
            dataPacket[DATA_OFFSET + firstChannel] = value;
            firstChannel++;
        }
    }

    void setDMXValue(int channel, int value) {
        if (channel > 0 && channel <= 512) {
            if (value > 255) {
                value = 255;
            }
            if (value < 0) {
                value = 0;
            }
            dataPacket[DATA_OFFSET + channel - 1] = (byte) value;
        }
    }

